package Loops;

public class ForLoop {
    public static void main(String[] args) {
        int n=5;

        for(int i=1;i<=n;i++){
            System.out.println("Java is language");
     
        }


        int [] number = {1,2,3,3,4};

        for (int i : number) {
            System.out.println(i);
        }
    }
}
