public class Unary {

    public static void main(String[] args) {
        int a=12;

        System.out.println("Value of a++ "+ a++);
        System.out.println("Value of a-- "+ a--);
     
        System.out.println("Value of ++a "+ ++a);
        System.out.println("Value of --a "+ --a);
    }
    
}
