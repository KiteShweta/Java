public class Assignment {
    public static void main(String[] args) {
        

        int a;
        a=10;//assignment

        System.out.println("Using += Operator "+(a+=5));
        System.out.println("Using -= Operator "+(a-=5));
        System.out.println("Using *= Operator "+(a*=5));
        System.out.println("Using /= Operator "+(a/=5));
        System.out.println("Using %= Operator "+(a%=5));
    }
}
